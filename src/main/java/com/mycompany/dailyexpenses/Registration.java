    private void update() {
        String username = textUserName.getText().trim();
        char[] pass = textPassword.getPassword();
        String password = new String(pass);
        try {
            double budget = Double.parseDouble(textBudget.getText().trim());
            try {
                Class.forName("org.postgresql.Driver");
                conn = DriverManager.getConnection(Constants.url, Constants.user, Constants.password);
                ps = conn.prepareStatement(UPDATE_USER_SQL);
                ps.setDouble(1, budget);
                ps.setString(2, password);
                ps.setString(3, username);
                ps.executeUpdate();
                ps.close();
                conn.close();
                JOptionPane.showMessageDialog(jBtnUpdate, "Data Saved");
                clear();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(jBtnUpdate, "Enter a budget");
        }
    }

