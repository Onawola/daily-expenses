    public void insert() {
        String username = textUserName.getText().trim();
        char[] pass = textPassword.getPassword();
        String password = new String(pass);
        try {
            double budget = Double.parseDouble(textBudget.getText().trim());
            try {
                Class.forName("org.postgresql.Driver");
                conn = DriverManager.getConnection(Constants.url, Constants.user, Constants.password);
                ps = conn.prepareStatement(INSERT_USER_SQL);
                ps.setString(1, username);
                ps.setDouble(2, budget);
                ps.setString(3, password);
                ps.executeUpdate();
                ps.close();
                conn.close();
                JOptionPane.showMessageDialog(jBtnSignUp, "Data Saved Successfully");
                clear();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(jBtnSignUp, "Enter a valid budget");
        }
    }

